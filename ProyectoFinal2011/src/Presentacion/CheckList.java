    /** Creates new form CheckList */
    public CheckList() {
        initComponents();
        iniciar();

            setListData(new CheckListItem[] {
            new CheckListItem("Proveedor 1"),
            new CheckListItem("Proveedor 2"),
            new CheckListItem("Proveedor 3"),
            new CheckListItem("Proveedor 4"),
            new CheckListItem("Proveedor 4")});
    }



    private void iniciar()
    {
      // Use a CheckListRenderer (see below)
      // to renderer list cells

      setCellRenderer(new CheckListRenderer());
      setSelectionMode(ListSelectionModel.SINGLE_SELECTION);

      // Add a mouse listener to handle changing selection

      addMouseListener(new MouseAdapter()
      {
         public void mouseClicked(MouseEvent event)
         {
            JList list = (JList) event.getSource();

            // Get index of item clicked

            int index = list.locationToIndex(event.getPoint());
            CheckListItem item = (CheckListItem)
               list.getModel().getElementAt(index);

            // Toggle selected state

            item.setSelected(! item.isSelected());

            // Repaint cell

            list.repaint(list.getCellBounds(index, index));
         }
      });
    }


