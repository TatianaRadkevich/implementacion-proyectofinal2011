    private void btnGenerarReporteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerarReporteActionPerformed
        if(validar()){

        if(cmbProveedor.getSelectedIndex()!=-1){
        int id =   ((Proveedor)cmbProveedor.getSelectedItem()).getId();
        jasper.addParameter("ID_PROVEEDOR",id);
        }
        else
        jasper.addParameter("ID_PROVEEDOR",null);

        jasper.addParameter("FECHA_DESDE", dtcFechaDesde.getDate());
        jasper.addParameter("FECHA_HASTA", dtcFechaHasta.getDate());
        jasper.runReporte();
        this.setVisible(false);
        }
    }//GEN-LAST:event_btnGenerarReporteActionPerformed

