    private void btnSalir1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalir1ActionPerformed
        Empleado temp=tmDetalle.getSeletedObject();
        
        if(temp==null)
        {
            Mensajes.mensajeErrorGenerico("Debe seleccionar un empleado");
            return;
        }

        asistencia = new AsistenciaEmpleado();
        asistencia.setEmpleado(temp);
        asistencia.setFecAsistencia(Utilidades.getFechaActual());
        asistencia.setHoraIngreso(Utilidades.parseHora(Utilidades.getFechaActual()));
        gestor.guardarAsistencia(asistencia);
        txtFechaActual.setText(Utilidades.parseFecha(new Date()));
        txtHoraActual.setText(Utilidades.parseHora(new Date()));
}//GEN-LAST:event_btnSalir1ActionPerformed

